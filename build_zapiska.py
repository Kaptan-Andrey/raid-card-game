# ---------- помощники ----------
def body(text, bold_lead=None, indent=True, align=None):
    p = doc.add_paragraph()
    if not indent:
        p.paragraph_format.first_line_indent = Cm(0)
    if align is not None:
        p.alignment = align
    if bold_lead:
        r = p.add_run(bold_lead)
        r.bold = True
        text = text
    p.add_run(text)
    return p

def bullets(items, numbered=False):
    style = 'List Number' if numbered else 'List Bullet'
    for it in items:
        p = doc.add_paragraph(style=style)
        p.paragraph_format.line_spacing = 1.5
        if isinstance(it, tuple):
            r = p.add_run(it[0]); r.bold = True
            p.add_run(it[1])
        else:
            p.add_run(it)

def h1(t): doc.add_heading(t, level=1)
def h2(t): doc.add_heading(t, level=2)

def add_field(paragraph, instr):
    """Вставка поля Word (для оглавления)."""
    r = paragraph.add_run()
    fb = OxmlElement('w:fldChar'); fb.set(qn('w:fldCharType'), 'begin'); r._r.append(fb)
    it = OxmlElement('w:instrText'); it.set(qn('xml:space'), 'preserve'); it.text = instr; r._r.append(it)
    fs = OxmlElement('w:fldChar'); fs.set(qn('w:fldCharType'), 'separate'); r._r.append(fs)
    r2 = paragraph.add_run('Обновите поле: правый клик → «Обновить поле» (F9)')
    fe = OxmlElement('w:fldChar'); fe.set(qn('w:fldCharType'), 'end')
    r3 = paragraph.add_run(); r3._r.append(fe)

